    private void verificarLogin() {
        String apelido = jTxtApelido.getText();
        String senha = new String(jPswSenha.getText());
        
        // Validações
        if (apelido.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Coloca apelido");
            jTxtApelido.requestFocus();
            
        }
        
        if (senha.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Coloca senha!");
            jPswSenha.requestFocus();
            
        }
        
        // Autenticar no banco
        UsuariosDAO usuariosDAO = new UsuariosDAO();
        usuarioLogado = usuariosDAO.login(apelido, senha);
        
        if (usuarioLogado != null) {
            JOptionPane.showMessageDialog(this, "Login realizado");
            JFrmPrincipal jFrmPrincipal = new JFrmPrincipal();
            jFrmPrincipal.setVisible(true); 
        } else {
            JOptionPane.showMessageDialog(this, 
                "Login errado", 
                "Erro de login", 
                JOptionPane.ERROR_MESSAGE);
            jPswSenha.setText("");
            jPswSenha.requestFocus();
        }
    }

